/* begins communication with the BMI088 accel */
int Bmi088Accel::begin()
{
  if (_useSPI) {
    // setting CS pin to output
    pinMode(_csPin,OUTPUT);
    digitalWrite(_csPin,HIGH);
    // begin SPI communication
    _spi->begin();
  } else {
    /* starting the I2C bus */
    _i2c->begin();
    /* setting the I2C clock */
    _i2c->setClock(_i2cRate);
  }
  /* check device id */
  if (!isCorrectId()) {
    return -1;
  }
  /* soft reset */
  softReset();
  /* enable the accel */
  if (!setPower(true)) {
    return -2;
  }
  /* enter active mode */
  if (!setMode(true)) {
    return -3;
  } 
  /* self test */
  if (!selfTest()) {
    return -4;
  }
  /* soft reset */
  softReset();
  /* enable the accel */
  if (!setPower(true)) {
    return -5;
  }
  /* enter active mode */
  if (!setMode(true)) {
    return -6;
  } 
  delay(50);
  /* set default range */
  if (!setRange(RANGE_24G)) {
    return -7;
  }
  /* set default ODR */
  if (!setOdr(ODR_1600HZ_BW_280HZ)) {
    return -8;
  }
  /* check config errors */
  if (isConfigErr()) {
    return -9;
  }
  /* check fatal errors */
  if (isFatalErr()) {
    return -10;
  }
  return 1;
}

